#include "../headers/push_swap.h"

void	ft_sort_3_numbers(t_pushswap *n)
{
	if ((n->a[0] > n->a[1]) && (n->a[1] < n->a[2]) && (n->a[0] < n->a[2]))
		sa(n);
	if ((n->a[0] > n->a[1]) && (n->a[1] > n->a[2]) && (n->a[0] > n->a[2]))
	{
		sa(n);
		rra(n);
	}
	if ((n->a[0] > n->a[1]) && (n->a[1] < n->a[2]) && (n->a[0] > n->a[2]))
		ra(n);
	if ((n->a[0] < n->a[1]) && (n->a[1] > n->a[2]) && (n->a[0] < n->a[2]))
	{
		sa(n);
		ra(n);
	}
	if ((n->a[0] < n->a[1]) && (n->a[1] > n->a[2]) && (n->a[0] > n->a[2]))
		rra(n);
}
